if (!function_exists('do_pagination')){
    function do_pagination($current, $total, $count){
        $begin = $current - $count;
        $end = $current + $count;
                
        $result = array();
        
        if ($begin < 1){
            $begin = 1;
        }
        if ($end > $total){
            $end = $total;
        }
        
        if ($begin > 1){
            $result[] = 1;
            $result[] = false;
        }
        
        for ($page = $begin; $page <= $end; $page++){
            $result[] = $page;
        }
        
        if ($end < $total){
            $result[] = false;
            $result[] = $total;
        }
        
        return $result;
    }
}
if (!function_exists('set_get_params')){
    function set_get_params($params){
        foreach ($params as $key => $value){
            $_GET[$key] = $value;
        }
        return http_build_query($_GET);
    }
}
if (!function_exists('explode_timestamp')){
    function explode_timestamp($timestamp){
        $days = floor($timestamp / (24 * 3600));
        $timestamp = $timestamp - ($days * 24 * 3600);
        $hours = floor($timestamp / 3600);        
        $timestamp = $timestamp - ($hours * 3600);
        $minutes = floor($timestamp / 60);
        return array('days' => $days, 'hours' => $hours, 'minutes' => $minutes);
    }
}